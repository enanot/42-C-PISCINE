int	*parse(char **argv, int nums[])
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (argv[1][i] != '\0')
	{
		if (i % 2 == 0)
		{
			nums[j] = argv[1][i] - '0';
			j++;
		}
		i++;
	}
	return (nums);
}
